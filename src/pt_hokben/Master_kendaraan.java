    private void exportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportActionPerformed
        // TODO add your handling code here:
        try {

        /*Nama file excell*/
            String filename = "C:/ReportKendaraan.xls" ;
            HSSFWorkbook workbook = new HSSFWorkbook();

        /*menentukan sheet*/
            HSSFSheet sheet = workbook.createSheet("FirstSheet"); 
            try {
           String  sql = "select * from master_kendaraan " ;
            int i =  1 ; 
            java.sql.Connection conn = (Connection)koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            HSSFRow rowhead = sheet.createRow((short)0);  /*index row 0*/
                 rowhead.createCell(0).setCellValue("No");  /*column 0*/
                 rowhead.createCell(1).setCellValue("Kode Kendaraan");  /*column 1*/
                 rowhead.createCell(2).setCellValue("Merek Kendaraan");  /*column 1*/
                 rowhead.createCell(3).setCellValue("Jenis Kendaraaan");  /*column 2*/
                 rowhead.createCell(4).setCellValue("No Polisi");  /*column 2*/
                 rowhead.createCell(5).setCellValue("Masa Kendaraaan");  /*column 2*/
                 rowhead.createCell(6).setCellValue("Kondisi Kendaraaan");  /*column 2*/
            while(res.next()){
                    HSSFRow row   = sheet.createRow((short)i);   /*index row 1*/
                    row.createCell(0).setCellValue(i);  /*column 0*/
                    row.createCell(1).setCellValue(res.getString(1));  /* column  1*/
                    row.createCell(2).setCellValue(res.getString(2));  /* column  1*/
                    row.createCell(3).setCellValue(res.getString(3));  /* column 2*/
                    row.createCell(4).setCellValue(res.getString(4));  /* column 2*/
                    row.createCell(5).setCellValue(res.getString(5));  /* column 2*/
                    row.createCell(6).setCellValue(res.getString(6));  /* column 2*/
                i++ ;
            }
        }catch(Exception e){
            
        }
            FileOutputStream fileOut = new FileOutputStream(filename);

            /*menulis file*/
            workbook.write(fileOut);

           /*menutup koneksi*/
            fileOut.close();
               JOptionPane.showMessageDialog(null, "Berhasil di export di C:/ReportKendaraan.xls");
        } catch ( Exception ex ) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_exportActionPerformed

    private void pdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pdfActionPerformed
        // TODO add your handling code here:
        try {
            String path = "D://KendaraanReport.pdf" ;
            String  sql = "select * from master_kendaraan " ;
            java.sql.Connection conn = (Connection)koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            /* Step-2: Initialize PDF documents - logical objects */
        Document my_pdf_report = new Document();
        PdfWriter.getInstance(my_pdf_report, new FileOutputStream(path));
        my_pdf_report.open();            
        my_pdf_report.add(new Paragraph("Laporan Master Kendaraan",FontFactory.getFont(FontFactory.TIMES_BOLD, 18, Font.BOLD, BaseColor.BLUE)));
        my_pdf_report.add(new Paragraph(new Date().toString()));
        my_pdf_report.add(new Paragraph("-------------------------------------------------------------------"
                + "--------------------------------------------------------------"));
        my_pdf_report.add(new Paragraph("\n"));
        //we have four columns in our table
        PdfPTable my_report_table = new PdfPTable(6);
        //create a cell object
        PdfPCell table_cell;
        PdfPCell table_cell2;
        
        
        table_cell2=new PdfPCell(new Phrase("Kode Kendaraan"));
        table_cell2.setBackgroundColor(BaseColor.CYAN);
        my_report_table.addCell(table_cell2);
        table_cell2=new PdfPCell(new Phrase("Merek"));
        table_cell2.setBackgroundColor(BaseColor.CYAN);
        my_report_table.addCell(table_cell2);
        table_cell2=new PdfPCell(new Phrase("Jenis"));
        table_cell2.setBackgroundColor(BaseColor.CYAN);
        my_report_table.addCell(table_cell2);
        table_cell2=new PdfPCell(new Phrase("No Polisi"));
        table_cell2.setBackgroundColor(BaseColor.CYAN);
        my_report_table.addCell(table_cell2);
        table_cell2=new PdfPCell(new Phrase("Masa"));
        table_cell2.setBackgroundColor(BaseColor.CYAN);
        my_report_table.addCell(table_cell2);
        table_cell2=new PdfPCell(new Phrase("Kondisi"));
        table_cell2.setBackgroundColor(BaseColor.CYAN);
        my_report_table.addCell(table_cell2);

        
        while (res.next()) {                
            String kode         = res.getString("kode_kendaraan");
            table_cell          = new PdfPCell(new Phrase(kode));
            my_report_table.addCell(table_cell);
            
            String merk         = res.getString("merk_kendaraan");
            table_cell          = new PdfPCell(new Phrase(merk));
            my_report_table.addCell(table_cell);
            
            String jenis        = res.getString("jenis_kendaraan");
            table_cell          = new PdfPCell(new Phrase(jenis));
            my_report_table.addCell(table_cell);
            
            String nopolisi     = res.getString("no_polisi");
            table_cell          = new PdfPCell(new Phrase(nopolisi));
            my_report_table.addCell(table_cell);
            
            String masa         = res.getString("masa_kendaraan");
            table_cell          = new PdfPCell(new Phrase(masa));
            my_report_table.addCell(table_cell);
            
            String kondisi      = res.getString("kondisi_kendaraan");
            table_cell=new PdfPCell(new Phrase(kondisi));
            my_report_table.addCell(table_cell);
            
         }
        /* Attach report table to PDF */
        my_pdf_report.add(my_report_table);                       
        my_pdf_report.close();

        /* Close all DB related objects */
        JOptionPane.showMessageDialog(null,"Disimpan " + path );
        }catch(Exception e){
            
        }
        
    }//GEN-LAST:event_pdfActionPerformed


    public void loadData(String data){
       DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Kendaraan");
        model.addColumn("Merk Kendaraan");
        model.addColumn("Jenis Kendaraan");
        model.addColumn("No Polisi");
        model.addColumn("Masa Kendaraan");
        model.addColumn("Kondisi Kendaraan");
        
        //tampilkan data makanan kedalam table 
        try {
            String sql = "" ;

            if(data == null ){
                sql = "Select * from master_kendaraan   " ;
            }else {
                sql = "Select * from master_kendaraan where "
                        + "kode_kendaraan like  '%"+ data +"%' or merk_kendaraan like '%"+ data +"%'  "
                        + "or kondisi_kendaraan like '%"+ data +"%'   "  ;                
            }
            
            java.sql.Connection conn = (Connection)koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[] {res.getString(1) ,  res.getString(2) , res.getString(3) , res.getString(4)
                , res.getString(5) , res.getString(6) } );
            }
            kendaraan.setModel(model);
        }catch(Exception e){
            
        } 
    }
